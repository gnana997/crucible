// Command gen writes the wire-protocol conformance fixtures: recorded frame
// streams (*.bin) plus a manifest.json describing every frame in them. SDK
// implementations in any language decode the .bin files and compare against
// the manifest — no daemon or KVM needed.
//
// The streams are produced by the real codec (sdk/wire) and the manifest is
// derived by re-decoding the produced bytes, so fixtures cannot disagree
// with the implementation. Output is fully deterministic; the CI
// codegen-drift job regenerates and fails on any diff.
//
// Regenerate with `make gen-fixtures` (or `go run ./sdks/fixtures/gen`).
package main

import (
	"bytes"
	"crypto/sha256"
	"encoding/binary"
	"encoding/hex"
	"encoding/json"
	"errors"
	"flag"
	"fmt"
	"io"
	"log"
	"os"
	"path/filepath"
	"strings"
	"unicode/utf8"

	"github.com/gnana997/crucible/sdk/wire"
)

// manifest is the top-level shape of manifest.json.
type manifest struct {
	Comment    string          `json:"_comment"`
	Header     headerSpec      `json:"header"`
	FrameTypes map[string]byte `json:"frame_types"`
	MaxPayload int             `json:"max_payload_size"`
	Fixtures   []fixtureEntry  `json:"fixtures"`
}

type headerSpec struct {
	Size           int    `json:"size"`
	TypeOffset     int    `json:"type_offset"`
	ReservedOffset int    `json:"reserved_offset"`
	ReservedLen    int    `json:"reserved_len"`
	LenOffset      int    `json:"len_offset"`
	LenEncoding    string `json:"len_encoding"`
}

type fixtureEntry struct {
	File        string       `json:"file"`
	Description string       `json:"description"`
	Direction   string       `json:"direction"` // guest_to_host | host_to_guest
	Invalid     bool         `json:"invalid,omitempty"`
	Expect      string       `json:"expect,omitempty"` // for invalid fixtures
	Frames      []frameEntry `json:"frames,omitempty"`
}

type frameEntry struct {
	Type          string          `json:"type"`
	TypeByte      byte            `json:"type_byte"`
	PayloadLen    int             `json:"payload_len"`
	PayloadSHA256 string          `json:"payload_sha256"`
	PayloadUTF8   string          `json:"payload_utf8,omitempty"` // set when short and printable
	ExecResult    json.RawMessage `json:"exec_result,omitempty"`  // set on exit frames: the parsed payload
}

var typeNames = map[byte]string{
	wire.FrameStdout:     "stdout",
	wire.FrameStderr:     "stderr",
	wire.FrameExit:       "exit",
	wire.FrameStdin:      "stdin",
	wire.FrameStdinClose: "stdin_close",
}

func main() {
	out := flag.String("out", "sdks/fixtures", "output directory")
	flag.Parse()

	m := manifest{
		Comment: "GENERATED by sdks/fixtures/gen — do not edit. Recorded crucible wire-protocol " +
			"frame streams for SDK conformance tests; see docs/wire.md.",
		Header: headerSpec{
			Size: wire.FrameHeaderSize, TypeOffset: 0,
			ReservedOffset: 1, ReservedLen: 3,
			LenOffset: 4, LenEncoding: "uint32_be",
		},
		FrameTypes: map[string]byte{},
		MaxPayload: wire.MaxPayloadSize,
	}
	for b, n := range typeNames {
		m.FrameTypes[n] = b
	}

	for _, f := range buildFixtures() {
		if err := os.WriteFile(filepath.Join(*out, f.name), f.data, 0o644); err != nil {
			log.Fatalf("write %s: %v", f.name, err)
		}
		entry := fixtureEntry{
			File: f.name, Description: f.desc, Direction: f.direction,
			Invalid: f.invalid, Expect: f.expect,
		}
		if !f.invalid {
			frames, err := describeFrames(f.data)
			if err != nil {
				log.Fatalf("%s: re-decode of generated stream failed: %v", f.name, err)
			}
			entry.Frames = frames
		}
		m.Fixtures = append(m.Fixtures, entry)
	}

	buf, err := json.MarshalIndent(m, "", "  ")
	if err != nil {
		log.Fatalf("marshal manifest: %v", err)
	}
	buf = append(buf, '\n')
	if err := os.WriteFile(filepath.Join(*out, "manifest.json"), buf, 0o644); err != nil {
		log.Fatalf("write manifest: %v", err)
	}
	fmt.Printf("wrote %d fixtures + manifest.json to %s\n", len(m.Fixtures), *out)
}

// describeFrames decodes a generated stream and returns its manifest
// entries — the manifest is derived from the bytes, never hand-stated.
func describeFrames(data []byte) ([]frameEntry, error) {
	var out []frameEntry
	r := bytes.NewReader(data)
	for {
		f, err := wire.ReadFrame(r)
		if errors.Is(err, io.EOF) {
			return out, nil
		}
		if err != nil {
			return nil, err
		}
		sum := sha256.Sum256(f.Payload)
		e := frameEntry{
			Type: typeNames[f.Type], TypeByte: f.Type,
			PayloadLen: len(f.Payload), PayloadSHA256: hex.EncodeToString(sum[:]),
		}
		if len(f.Payload) <= 256 && utf8.Valid(f.Payload) && !strings.ContainsRune(string(f.Payload), 0) {
			e.PayloadUTF8 = string(f.Payload)
		}
		if f.Type == wire.FrameExit {
			var res wire.ExecResult
			if err := json.Unmarshal(f.Payload, &res); err != nil {
				return nil, fmt.Errorf("exit frame payload is not ExecResult JSON: %w", err)
			}
			e.ExecResult = json.RawMessage(f.Payload)
		}
		out = append(out, e)
	}
}

type fixture struct {
	name, desc, direction string
	data                  []byte
	invalid               bool
	expect                string
}

func buildFixtures() []fixture {
	exitPayload := func(res wire.ExecResult) []byte {
		b, err := json.Marshal(res)
		if err != nil {
			log.Fatalf("marshal ExecResult: %v", err)
		}
		return b
	}
	stream := func(write func(fw *wire.FrameWriter)) []byte {
		var buf bytes.Buffer
		write(wire.NewFrameWriter(&buf))
		return buf.Bytes()
	}

	// A >2×MaxPayloadSize logical write, to pin the chunking rule. The
	// pattern is fixed so output is deterministic.
	bigPayload := bytes.Repeat([]byte("0123456789abcdef"), (2*wire.MaxPayloadSize+5)/16+1)[:2*wire.MaxPayloadSize+5]

	return []fixture{
		{
			name: "exec_basic.bin", direction: "guest_to_host",
			desc: "Minimal one-shot exec response: one stdout frame, then the terminal exit frame (exit 0).",
			data: stream(func(fw *wire.FrameWriter) {
				_ = fw.WriteFrame(wire.FrameStdout, []byte("hello world\n"))
				_ = fw.WriteFrame(wire.FrameExit, exitPayload(wire.ExecResult{ExitCode: 0, DurationMs: 12}))
			}),
		},
		{
			name: "exec_interleaved_usage.bin", direction: "guest_to_host",
			desc: "stdout and stderr interleaved (order must be preserved), exit frame carries a full ResourceUsage.",
			data: stream(func(fw *wire.FrameWriter) {
				_ = fw.WriteFrame(wire.FrameStdout, []byte("out 1\n"))
				_ = fw.WriteFrame(wire.FrameStderr, []byte("err 1\n"))
				_ = fw.WriteFrame(wire.FrameStdout, []byte("out 2\n"))
				_ = fw.WriteFrame(wire.FrameStderr, []byte("err 2\n"))
				_ = fw.WriteFrame(wire.FrameExit, exitPayload(wire.ExecResult{
					ExitCode: 0, DurationMs: 341,
					Usage: &wire.ResourceUsage{
						CPUUserMs: 120, CPUSysMs: 31, PeakMemoryBytes: 9437184,
						PageFaultsMajor: 3, ContextSwitchesInvoluntary: 17,
						IOReadBytes: 65536, IOWriteBytes: 4096,
					},
				}))
			}),
		},
		{
			name: "exec_exit_only.bin", direction: "guest_to_host",
			desc: "No output at all — the stream is just the terminal exit frame (exit 7).",
			data: stream(func(fw *wire.FrameWriter) {
				_ = fw.WriteFrame(wire.FrameExit, exitPayload(wire.ExecResult{ExitCode: 7, DurationMs: 3}))
			}),
		},
		{
			name: "exec_agent_error.bin", direction: "guest_to_host",
			desc: "Agent-side failure: exit frame with error set and exit_code -1 (command never ran).",
			data: stream(func(fw *wire.FrameWriter) {
				_ = fw.WriteFrame(wire.FrameExit, exitPayload(wire.ExecResult{
					ExitCode: -1, DurationMs: 1, Error: "start: exec: \"nope\": executable file not found in $PATH",
				}))
			}),
		},
		{
			name: "exec_timeout.bin", direction: "guest_to_host",
			desc: "Timeout kill: partial stdout, then exit with timed_out, SIGKILL, exit_code -1.",
			data: stream(func(fw *wire.FrameWriter) {
				_ = fw.WriteFrame(wire.FrameStdout, []byte("working...\n"))
				_ = fw.WriteFrame(wire.FrameExit, exitPayload(wire.ExecResult{
					ExitCode: -1, DurationMs: 2004, Signal: "SIGKILL", TimedOut: true,
				}))
			}),
		},
		{
			name: "exec_chunked.bin", direction: "guest_to_host",
			desc: "Chunking rule: one logical stdout write of 2×max_payload_size+5 bytes arrives as three " +
				"stdout frames (max, max, 5) that decoders must treat as one continuous stream.",
			data: stream(func(fw *wire.FrameWriter) {
				_ = fw.WriteFrame(wire.FrameStdout, bigPayload)
				_ = fw.WriteFrame(wire.FrameExit, exitPayload(wire.ExecResult{ExitCode: 0, DurationMs: 88}))
			}),
		},
		{
			name: "stdin_session.bin", direction: "host_to_guest",
			desc: "The client side of an interactive session: two stdin chunks then stdin_close (empty payload). " +
				"Encoder tests produce these exact bytes; the guest replies with a guest_to_host stream.",
			data: stream(func(fw *wire.FrameWriter) {
				_ = fw.WriteFrame(wire.FrameStdin, []byte("echo hi\n"))
				_ = fw.WriteFrame(wire.FrameStdin, []byte("exit\n"))
				_ = fw.WriteFrame(wire.FrameStdinClose, nil)
			}),
		},
		{
			name: "invalid_truncated_header.bin", direction: "guest_to_host", invalid: true,
			desc:   "Stream ends mid-header (4 of 8 bytes). Decoders must fail with an unexpected-EOF error, not return a frame.",
			expect: "decode error: truncated header (Go: io.ErrUnexpectedEOF)",
			data:   []byte{wire.FrameStdout, 0, 0, 0},
		},
		{
			name: "invalid_truncated_payload.bin", direction: "guest_to_host", invalid: true,
			desc:   "Header declares a 16-byte payload but only 4 bytes follow. Decoders must fail with an unexpected-EOF error.",
			expect: "decode error: truncated payload (Go: io.ErrUnexpectedEOF)",
			data: func() []byte {
				var b bytes.Buffer
				var hdr [wire.FrameHeaderSize]byte
				hdr[0] = wire.FrameStdout
				binary.BigEndian.PutUint32(hdr[4:], 16)
				b.Write(hdr[:])
				b.WriteString("only")
				return b.Bytes()
			}(),
		},
		{
			name: "invalid_oversize_frame.bin", direction: "guest_to_host", invalid: true,
			desc:   "Header declares a payload of max_payload_size+1. Decoders must reject the frame before reading the payload.",
			expect: "decode error: frame size exceeds max_payload_size",
			data: func() []byte {
				var hdr [wire.FrameHeaderSize]byte
				hdr[0] = wire.FrameStdout
				binary.BigEndian.PutUint32(hdr[4:], uint32(wire.MaxPayloadSize+1))
				return hdr[:]
			}(),
		},
	}
}
